//------------ Блок парсера сценариев -------------------------------------------------------------------------------------------------------
void DataSerial::scenarioParser (String data){                                     // получаем данные
  String tempdata;
  String cycledata = data;                                  // сохраняем набор команд на случай зацикливания !
  dataIn = "";                                              // очищаем входные данные
  data_flag = false;
  do {                                                     
    if (stop){                                              // если флаг СТОП
		Flags_Array[3] = false;                                 // отмена зацикливания сценариев
		cycledata = "";                                         // переменную с набором команд тоже очищаем
		break;                                                  // выходим из цикла                     
    } 
    tempdata = data.substring(0, 1);                           // пишем в переменную первый символ
    data.remove(0,1);                                          // удаляем первый символ из набора команд
    switch (tempdata.charAt(0)) {                              // ищем совпадение по символу
      
      case 0x4F:                                                    // (ASCII 'O') включение контроллера лестницы
        myStaircase.actionSTOP();                                         // вызываем функцию СТОП
        Flags_Array[0] = false;                                     // выход из режима ожидания (флаг StandBy)
        outData (4, "StandByOFF", 1);                               // сообщение оператору, режим выключен
        Flags_Array[11] = false;                                    // снимаем флаг режима Охраны
        outData (4, "SecurityOFF", 0);                                 // сообщение оператору, что флаг режима Охраны снят
        Flags_Array[12] = false;                                    // снимаем флаг контроля сенсоров в режиме Охраны
        Flags_Array[13] = false;                                    // снимаем флаг SOS в режиме Охраны
        break;
      case 0x6F:                                                    // (ASCII 'o') выключение контроллера лестницы
        Tlc.setAll(0);                                                // тушим все ступени
        Tlc.update();
        standByTime = millis();											// запоминаем время старта режима StandBy
        Flags_Array[0] = true;                                          // переход в режим ожидания (флаг StandBy)
        outData (4, "StandByON", 1);                                    // сообщение оператору, режим включен
        break;
      case 0x50:                                                    // (ASCII 'P') резерв

        break;
      case 0x58:                                                    // (ASCII 'X') запрос состояния контроллера лестницы
        dataIn = "";
        statusMode();                                                 // функция ответа на запрос 
        break;
      case 0x55:                                                    // (ASCII 'U') команда включения вверх
        Flags_Array[4] = true;
        myStaircase.move(0, data_Array[10], data_Array[14], 1, 1, 0);                     // включаем ступени последовательно вверх
        Flags_Array[4] = false;
        break;
      case 0x75:                                                    // (ASCII 'u') команда выключения вверх
        Flags_Array[4] = true;
        myStaircase.move(data_Array[10], 0, data_Array[14], 1, 0, 0);                     // выключаем ступени последовательно вверх
        Flags_Array[4] = false;
        break;
      case 0x44:                                                    // (ASCII 'D') команда включения вниз
        Flags_Array[5] = true;
        myStaircase.move(0, data_Array[10], data_Array[14], 0, 1, 0);                   // включаем ступени последовательно вниз
        Flags_Array[5] = false;
        break;
      case 0x64:                                                    // (ASCII 'd') команда выключения вниз
        Flags_Array[5] = true;
        myStaircase.move(data_Array[10], 0, data_Array[14], 0, 0, 0);                   // выключаем ступени последовательно вниз
        Flags_Array[5] = false;
        break;
      case 0x41:                                                    // (ASCII 'A') команда включения всех ступеней
        Flags_Array[6] = true;
        lightEffects.all(data_Array[10], 1);                                       // включаем все ступени разом
        break;
      case 0x61:                                                    // (ASCII 'a') команда выключения всех ступеней
        Flags_Array[6] = false;
        lightEffects.all(0, 0);                                                    // выключаем все ступени разом
        break;
      case 0x46:                                                    // (ASCII 'F') команда включения в разные стороны
        Flags_Array[7] = true;
        lightEffects.different(0, data_Array[10], data_Array[14], 1);              // включаем ступени от середины в разные стороны
        Flags_Array[7] = false;
        break;
      case 0x66:                                                    // (ASCII 'f') команда выключения в разные стороны
        Flags_Array[7] = true;
        lightEffects.different(data_Array[10], 0, data_Array[14], 0);              // выключаем ступени от середины в разные стороны
        Flags_Array[7] = false;
        break;
      case 0x54:                                                    // (ASCII 'T') команда включения навстречу
        Flags_Array[8] = true;
        lightEffects.towards(0, data_Array[10], data_Array[14], 1);                // включаем ступени навстречу с разных сторон
        Flags_Array[8] = false;
        break;
      case 0x74:                                                    // (ASCII 't') команда выключения навстречу
        Flags_Array[8] = true;
        lightEffects.towards(data_Array[10], 0, data_Array[14], 0);                // выключаем ступени навстречу с разных сторон
        Flags_Array[8] = false;
        break;
      case 0x53:                                                    // (ASCII 'S') SOS
        Flags_Array[9] = true;
        lightEffects.sos(4095, 0);                                                 // режим SOS, (максимальная яркость, зациклить- 1 или один раз- 0)
        Flags_Array[9] = false;
        break;
      case 0x4C:                                                    // (ASCII 'L') режим люминесцентной лампы
        Flags_Array[10] = true;
        lightEffects.luminescent(data_Array[10]);                                  // включаем все ступени с эффектом люминесцентной лампы
        Flags_Array[10] = false;
        break;
      case 0x43:                                                    // (ASCII 'C') зациклить сценарии
        Flags_Array[3] = true;
        break;
      case 0x63:                                                    // (ASCII 'c') отмена зацикливания сценариев
        Flags_Array[3] = false;
        break;
      default:                                                      // если совпадений нет
        outData (2, tempdata, 0);                         // сообщение оператору "Неверные данные"
        break;
    }     
    if (Flags_Array[3] && data == "") {                       // если установлен флаг зацикливания и команды закончились
      data = cycledata;                                     // восстанавливаем набор команд
    }
  } while (data != "");                                   // пока не кончатся команды
  stop = false;
}
