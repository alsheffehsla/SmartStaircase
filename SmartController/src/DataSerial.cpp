//------------ Блок парсера режима Охраны -------------------------------------------------------------------------------------------------------
void DataSerial::securityParser (String data){                                     // получаем данные
  String tempdata;
  dataIn = "";                                              // очищаем входные данные
  data_flag = false;
  do {                                                     
    tempdata = data.substring(0, 1);                           // пишем в переменную первый символ
    data.remove(0,1);                                          // удаляем первый символ из набора команд
    switch (tempdata.charAt(0)) {                              // ищем совпадение по символу
      case 0x5A:                                                    // (ASCII 'Z') включить контроль сенсоров
        Flags_Array[12] = true;                                         // флаг контроля сенсоров в режиме Охраны
        outData (4, "SensControlON", 0);                                       // сообщение оператору, что флаг контроля сенсоров выставлен
        break;
      case 0x45:                                                    // (ASCII 'E') включение SOS в режиме Охраны
        Flags_Array[13] = true;                                         // флаг SOS в режиме Охраны
        outData (4, "SOSsecurityON", 0);                                       // сообщение оператору, что флаг SOS выставлен
        break;
      default:                                                      // если совпадений нет
        outData (2, tempdata, 0);                         // сообщение оператору "Неверные данные"
        break;
    }     
  } while (data != "");                                   // пока не кончатся команды
  stop = false;
}
