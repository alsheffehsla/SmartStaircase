//------------ Блок проверки изменения в дефолтных настройках ------------------------------------------------------------------------------
bool DataSerial::arrayComparison (){            // 
  bool compare = Flags_Array[1];
  if (sizeof(default_Array) != sizeof(data_Array)) {
      compare = false;
  } else {
      int n = sizeof(default_Array) / sizeof(default_Array[0]);
      for (int i = 0; i < n; ++i) {
          if (default_Array[i] != data_Array[i]) {
              compare = false;
              break;
          }
        compare = true;
      }

  }
  compare ? Flags_Array[1] = 1 : Flags_Array[1] = 0;
  compare ? outData(4,"SettingsDefault",1) : outData(4,"SettingsNoDefault",1);

  return compare;
}
