// Нормализация команды (удаляем лишние пробелы и спецсимволы)
function normalizeCommand(command) {
    if (!command) return '';
    // Удаляем пробелы в начале и конце, удаляем \n, \r, \t
    return command.trim().replace(/[\n\r\t]/g, '');
}

// Функция преобразования команды в читаемый вид
function mapCommandToReadable(command) {
    if (!command) return command;

    // Нормализуем команду перед поиском
    const normalizedCommand = normalizeCommand(command);
    
    // Прямое сравнение - ищем точное совпадение
    if (COMMAND_MAP[normalizedCommand]) {
        return COMMAND_MAP[normalizedCommand];
    }
    
    // Если нет соответствия - возвращаем само сообщение
    return command;
}

// Экспорт функций
window.CommandMapper = {
    mapToReadable: mapCommandToReadable,
    normalize: normalizeCommand
};